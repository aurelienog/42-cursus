#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*last;

	if (!lst || !new)
		return ;
	last = malloc(sizeof(t_list));
	last = ft_lstlast(lst[0]);
	last->next = new;
}
